const LangCtx = createContext({ lang: "english", setLang: () => {} });
export function LangProvider({ children }) {
  const [lang, setLang] = useState("english");
  return createElement(LangCtx.Provider, { value: { lang, setLang } }, children);
}
export function useLang() { return useContext(LangCtx); }
export function useT() {
  const { lang } = useContext(LangCtx);
  return (k) => DICT[lang]?.[k] ?? Q_DICT[lang]?.[k] ?? D_DICT[lang]?.[k] ?? DICT.english[k] ?? Q_DICT.english[k] ?? D_DICT.english[k] ?? k;
}
